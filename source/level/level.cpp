DVECTOR	CLevel::getMapSize()
{
sLvlHdr		*LevelHdr=(sLvlHdr*)LevelBuffer;
DVECTOR		size;
sLayerHdr	*layer;

		layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->ActionLayer);
		size.vx=layer->Width;
		size.vy=layer->Height;

		return size;
}


