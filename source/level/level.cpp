/*****************************************************************************/
void 	CLevel::init()
{
#if defined(__USER_daveo__)
		if (Lvl>-1) s_globalLevelSelectThing=Lvl;
#endif
		if (s_globalLevelSelectThing>=LvlTableSize) s_globalLevelSelectThing=0;

		while (CFileIO::getFileSize(LvlTable[s_globalLevelSelectThing ].Level)<=3636)	// Dodgy blank level skip
		{
			s_globalLevelSelectThing++;
			if (s_globalLevelSelectThing>=LvlTableSize) s_globalLevelSelectThing=0;
		}

		for (int i=0; i<CLayerTile::LAYER_TILE_TYPE_MAX; i++)
		{
			TileLayers[i]=0;
		}
		CollisionLayer=0;
		MapPos.vx=0;		
		MapPos.vy=0;

		sLvlTab	*lvlTab;
		lvlTab=&LvlTable[s_globalLevelSelectThing];
		TileBank=(sTile *)CFileIO::loadFile(lvlTab->TileBank,"Tile Bank Data");
		LevelHdr=(sLvlHdr *)CFileIO::loadFile(lvlTab->Level,"Level Data");
		m_levelTPage=TPLoadTex(lvlTab->Tex);

		// Assuming this will come from the level data eventually.. (pkg)
		exitTrigger=new ("LevelExitTrigger") CLevelExitTrigger();
		exitTrigger->init();
// (pkg)
#if		defined(__USER_art__) || defined(__USER_paul__)
if(lvlTab->exitX==0&&lvlTab->exitY==0)
{
	PAUL_DBGMSG("-- NO LEVEL EXIT DEFINED FOR %d",s_globalLevelSelectThing);
	ASSERT(!"NO EXIT");
}
if(lvlTab->spawnX==268&&lvlTab->spawnY==160)
{
	PAUL_DBGMSG("-- NO LEVEL START DEFINED FOR %d",s_globalLevelSelectThing);
	ASSERT(!"NO START");
}
#endif
		ex=lvlTab->exitX;
		ey=lvlTab->exitY;
		ew=lvlTab->exitW-ex;
		eh=lvlTab->exitH-ey;
		s_playerSpawnPos.vx=lvlTab->spawnX;
		s_playerSpawnPos.vy=lvlTab->spawnY;
// (pkg)

		initLayers();
}

/*****************************************************************************/
void	CLevel::initLayers()
{
// Back

		if (LevelHdr->BackLayer) 
		{
			sLayerHdr	*Layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->BackLayer);
			CLayerTile	*NewLayer=new ("Back Layer") CLayerBack(Layer, TileBank);
			((CLayerBack*)NewLayer)->SetFrames(LvlTable[s_globalLevelSelectThing].Spr0,LvlTable[s_globalLevelSelectThing].Spr1);
			NewLayer->init(MapPos,3);
			TileLayers[CLayerTile::LAYER_TILE_TYPE_BACK]=NewLayer;
		}

// Mid
		if (LevelHdr->MidLayer) 
		{
			sLayerHdr	*Layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->MidLayer);
			CLayerTile *NewLayer=new ("Mid Layer") CLayerTile(Layer, TileBank);
			NewLayer->init(MapPos,2);
			TileLayers[CLayerTile::LAYER_TILE_TYPE_MID]=NewLayer;
		}

// Action
		if (LevelHdr->ActionLayer) 
		{
			sLayerHdr	*Layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->ActionLayer);
			CLayerTile *NewLayer=new ("Action Layer") CLayerTile3d(Layer, TileBank);
			NewLayer->init(MapPos,0);
			TileLayers[CLayerTile::LAYER_TILE_TYPE_ACTION]=NewLayer;
		}
/*
// Fore
		if (LevelHdr->ForeLayer) 
		{
			sLayerHdr	*Layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->ForeLayer);
			CLayerTile *NewLayer=new ("Fore Layer") CLayerTile(Layer, TileList, TriList, QuadList, VtxList);
			NewLayer->init();
			TileLayers[CLayerTile::LAYER_TILE_TYPE_FORE]=NewLayer;
		}
*/
// Collision
		if (LevelHdr->CollisionLayer) 
		{
			sLayerHdr	*Layer=(sLayerHdr*)MakePtr(LevelHdr,LevelHdr->CollisionLayer);
			CollisionLayer=new ("Collision Layer") CLayerCollision(Layer);
		}
}

/*****************************************************************************/
void	CLevel::shutdown()
{
		for (int i=0; i<CLayerTile::LAYER_TILE_TYPE_MAX; i++)
		{
			if (TileLayers[i])
			{
				TileLayers[i]->shutdown();
				delete TileLayers[i];
			}
		}

		TPFree(m_levelTPage);
		if (CollisionLayer)
		{
			CollisionLayer->shutdown();	MemFree(CollisionLayer);
		}
		MemFree(TileBank);
		MemFree(LevelHdr);
}

